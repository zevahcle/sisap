<h2> Instructions for camera-ready submissions </h2>

Your final paper can be up to <b>16 pages long</b> (demo 2 pages), formatted according to the LNCS
style rules, which must be followed strictly (<a href="http://www.springer.com/computer/lncs?SGWID=0-164-6-793341-0">Springer LNCS latex 2e
macros</a>). You will have to upload the sources, a pdf, and a copyright transfer form. The submissions will be made through the <a href="https://www.easychair.org/account/signin.cgi?conf=sisap2012">Easy Chair conference system</a>.

<br><br>
For a limited number of papers an extension of up to two extra pages will be allowed (i.e., max total 18 pages). One extra page costs 50 CAD, two pages 100 CAD. When interested, contact the PC chairs as soon as possible by email (if granted, check also the right option in the registration form).
<br><br>

At least one author of each accepted paper must be registered to the
conference by the deadline of June 1; the registration system will be opened
some days before. Submissions without registered authors will have to be
left out of the proceedings. In addition, at least one author of each paper
is expected to attend the conference and present the paper.

