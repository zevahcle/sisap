<h1>5th International Conference on Similarity Search and Applications (SISAP)</h1>                       
August 9-10, 2012, Toronto, Canada
<br />                        
<p></p><p>
The <i>International Conference on Similarity Search and Applications (SISAP)</i>
is a conference devoted to similarity searching, with emphasis on metric space
searching. It aims to fill in the gap left by the various scientific venues devoted
to similarity searching in spaces with coordinates, by providing a common forum
for theoreticians and practitioners around the problem of similarity searching in
general spaces (metric and non-metric) or using distance-based (as opposed to coordinate-based)
techniques in general.
</p>
<p>
SISAP aims to become an ideal forum to exchange real-world, challenging and exciting
examples of applications, new indexing techniques, common testbeds and benchmarks,
source code, and up-to-date literature through a Web page serving the similarity
searching community. Authors are expected to use the testbeds and code from the
SISAP Web site for comparing new applications, databases, indexes and algorithms.</p>

<p>
The proceedings of SISAP 2012 will be published in Springer's <b>Lecture Notes in Computer Science</b> series. A small selection of best  papers will be recommended for inclusion in a special issue of <b> Information Systems </b> journal (Elsevier) dedicated to this conference. SISAP 2012 is organized by <a href="http://www.fields.utoronto.ca/">Fields Institute in Toronto</a>

