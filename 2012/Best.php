<h2> Best papers </h2>

The editors of special issue of Information Systems (G. Navarro, V. Pestov) selected the four best papers of SISAP 2012 that were invited to the mentioned journal issue.
The selected papers are:

<ul>
<li>Bilegsaikhan Naidan and Magnus Lie Hetland<br>Static to Dynamic Transformation for Metric Indexing Structures </li>
<li>Juan Manuel Barrios, Benjamin Bustos and Tomas Skopal<br>Snake Table: A Dynamic Pivot Table for Streams of k-NN Searches </li>
<li>Yury Malkov, Alexander Ponomarenko, Andrey Logvinov and Vladimir Krylov<br>Scalable Distributed Algorithm for Approximate Nearest Neighbor Search Problem in High Dimensional General Metric Spaces </li>
<li>Santosh Vempala<br>Effective Principal Component Analysis</li>
</ul>
