<h2> Accommodation </h2>

The participants are asked to make their own accommodation booking.
There are a number of downtown hotels in the vicinity of the Fields institute that have a <a href="http://www.fields.utoronto.ca/resources/housing.html"> special rate for the Fields visitors</a>, so the
participants need to ask for a discount rate.

<br><br>   

Cheaper accommodation is available in <a href="http://www.fields.utoronto.ca/resources/housing/uoft_summer.html"> student residences  </a> on campus. In fact, the hotel rate slashes a regular price by at least half, so
it is a good deal. And the student accommodations are really cheap.

