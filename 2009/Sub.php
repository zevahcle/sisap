<h2> Presentation at the conference </h2> 
<table>
<tr>
<td style="width:150px"> <i> Full papers: </i> </td> </tr>
<tr>
<td/>
<td>
Accepted full papers are expected to be orally presented at the conference session. Each paper has allocated <b> 25 minutes </b> for presentation and <b> 5 minutes </b> for discussion.
</td>
</tr>
<tr>
<td> <i> Posters and demos: </i> </td> </tr>
<tr>
<tr>
<td/>
<td>
- Accepted poster papers are expected to be presented at the conference poster session. A suitable form of poster is a <b> large (A0-A1) piece of paper </b>  with concise schema sumarizing the paper contribution. We strongly discourage to use posters in form of printed papers.
</td>
</tr>
<tr>
<td/>
<td>
- An accepted demo paper is expected to be presented at the conference demo session. The demo presentation will be performed on an on-site notebook (or the presenter’s notebook) with a high-speed connection to internet. Optionally, there will be a data projector and a projection wall available.
</td>
</tr>
