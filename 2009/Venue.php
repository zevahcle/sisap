<h2> Conference venue </h2>

Prague is a beautiful city with a long medieval history. The workshop will be held at the School of Computer Science, Faculty of Mathematics and Physics, Charles University, which is located in "Mala Strana", the famous historical district of Prague. The building itself has been recently carefully renovated, reconstructing the architectural style of a Jesuit College.

The picture on the right is taken from the lower part of the Lesser Town Square (Malostranske namesti). The school is the white building in the middle. The Prague Castle on the horizon cannot be seen from the ground level. 

