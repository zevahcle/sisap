<h2>How to travel to the conference location</h2>

<h3>Travelling from the airport</h3>

<p>You can take:</p>
<ul>
 <li><b>a taxi</b>: say "Malostranske namesti, Faculty
    of Mathematics and Physics", the cost is approximately 600 CZK</li>
 <li><b>an airport minibus</b>: say "Malostranske
    namesti, Faculty of Mathematics and Physics", the cost is approximately
    360 CZK for up to 4 persons</li>
 <li><b>public transport</b>: take bus no. 119 to the
    final stop (Metro station Dejvicka, green line), then follow the
    instructions for travelling 'from anywhere in Prague' bellow.
    The single-transfer ticket for 75 minutes costs 26 CZK</li>
 </ul>

<p>You can find the web site of the Prague airport (code PRG) <a href="http://www.prg.aero/">here</a>.

<h3>Travelling from the railway station</h3>

  <p>There are several railway stations in Prague, but the following two are the most suitable:</p>
 <ul>
  <li><b>Hlavni nadrazi (Main station)</b>: this the most
    probable station of your arrival. The advantage is that this station is
    connected with the metro station of the same name (red line). Therefore
    just go to Metro and follow instructions for travelling 'from anywhere in
    Prague' bellow.</li>
  <li><b>Nadrazi Holesovice</b>: ask people for the stop
    of tram no. 12, take it to stop Malostranske namesti (7th stop,
    15min).</li>

</ul>

<h3>Travelling from anywhere in Prague</h3>

 <p>First, you need to find the nearest Metro station. Then, depending on the line
	A, B or C, choose one of the following ways:</p>
 <ul>
   <li><font color="green">Metro A (green line)</font>: go to Malostranska station, then
    follow by tram no. 12, 20, 22 to Malostranske namesti station (1st stop,
    2min)</li>
   <li><font color="orange">Metro B (yellow line)</font>: go to Narodni trida station, then
    follow by tram no. 22 Malostranske namesti (4th stop, 10min)</li>

   <li><font color="orange">Metro B (yellow line)</font>: go to Andel station, then follow
    by tram no. 12 Malostranske namesti (5th stop, 10min)</li>
    <li><font color="red">Metro C (red line)</font>: go to I.P. Pavlova station, then
    follow by tram no. 22 Malostranske namesti (7th stop, 15min)</li>
 </ul>
<p>A single-transfer ticket for 75 minutes costs 26 CZK. The ticket is valid for Metro, all trams, and buses with numbers less than 300. Note that single-transfer ticket must be stamped before starting the travel, i.e., either at the entrance to the Metro station or right after entering a tram or bus (look for yellow stamp machine on a column).</p>
<p>
You can find all information about the Prague public transportation <a href="http://www.dpp.cz/en/">here</a>.

