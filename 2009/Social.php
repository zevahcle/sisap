<h2> Social events </h2>

The conference fee (regardless of registration type) includes two social events - conference dinner and a trip to castle Konopiste.

<h1> Conference dinner </h1>


The conference dinner will take place on Saturday evening at the <a href="http://www.admiral-botel.cz/index.jsp?m=photo&s=index"> Admiral botel</a>, that is anchored by the banks of Vltava river. The dinner will be served at the summer board, providing a beautiful panorama of the Old city of Prague.

