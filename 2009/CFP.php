<h2> Call for papers </h2>

Authors are encouraged to submit previously unpublished papers on their research in similarity search and applications. The proceedings of SISAP 2009 will be published by <b style="color:#000077"> Conference Publishing Services </b>. A special issue with the extended versions of the four best SISAP 2009 papers will be invited for publication in <b style="color:#AA0000"> Information Systems </b> (Elsevier) (subject to additional reviewing).

<h3> Contributions to the conference should fall into the following categories: </h3> 

<table>
<tr> <td style="width:100px"> <i> Full papers: </i> <td> </tr>

<tr> <td> </td> <td>
<b style="color:#000077"> Basic techniques: </b> general methods that apply to arbitrary metric spaces, nonmetric or (dis)similarity spaces, or high-dimensional vector spaces. 
We strongly encourage that those be accompanied with an open-source implementation conforming the interface of the <a href="http://sisap.org/Metric_Space_Library.html"> Web site </a> and to be tested with a relevant subset of the spaces included in the Web site. This will favor uniform comparison and also permit comparing the technique with future developments. 
<br/><br/>
<b style="color:#000077"> Applied techniques: </b> methods that apply to specific similarity search problems. 
Those papers are also encouraged to contribute with the code and, especially, with the similarity computation function and the datasets related to the specific problem they address (if they are new), for the <a href="http://sisap.org/Metric_Space_Library.html"> Web site </a>. 
</td> </tr>

<tr> <td> <i> Posters: </i>  </td> </tr>

<tr> <td> </td> <td>
<b style="color:#007700"> Domain-specific spaces and similarities: </b> papers that challenge and motivate for searching in new spaces (i.e. domain-specific similarity search). 
This type of contribution is expected to provide a thorough study of the non/metric space properties (e.g., its intrinsic dimension) or the similarity measure, and must include code for the similarity computation and datasets for the <a href="http://sisap.org/Metric_Space_Library.html"> Web site </a>. For this kind of paper a real-world domain-specific application of similarity search is expected (e.g., multimedia databases, (bio)chemical & medical databases, biometric databases, scientific & sensory databases, etc.).
</td> </tr>

<tr> <td> <i>  Demos: </i>  </td> </tr>

<tr> <td> </td> <td>
<b style="color:#770000"> Similarity retrieval engines: </b> papers that present prototypical non-commercial engine entirely devoted to similarity search applications. 
The presentation should cover the domains of applicability (e.g., image retrieval system), the architecture, scalability, user interfaces, optional comparison with similar engines, etc. This type of contribution must include either an installation package of the demo application + database, or must be installed as a web application accessible to the SISAP PC.
</td> </tr>

</table>

